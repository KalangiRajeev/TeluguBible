package com.ikalangirajeev.telugubiblemessages.ui.bible.app;

public class Data {
    private String header;
    private String body;

    public Data(String header, String body) {
        this.header = header;
        this.body = body;
    }

    public String getHeader() {
        return header;
    }

    public String getBody() {
        return body;
    }
}
